import React, { useState, useEffect } from "react";
import CodeEditor from "./Components/CodeEditor";

function App() {
  const [html, setHtml] = useState<string>("");
  const [css, setCss] = useState<string>("");
  const [js, setJs] = useState<string>("");
  const [srcDoc, setSrcDoc] = useState<string>("");

  useEffect(() => {
    const timeout = setTimeout(() => {
      setSrcDoc(`
        <html>
          <body>${html}</body>
          <style>${css}</style>
          <script>${js}</script>
        </html>
      `);
    }, 250);

    return () => clearTimeout(timeout);
  }, [html, css, js]);

  return (
    <>
      <div className="pane top-pane">
        <CodeEditor
          Language="xml"
          Title="HTML"
          value={html}
          onChange={setHtml}
        />
        <CodeEditor Language="css" Title="CSS" value={css} onChange={setCss} />
        <CodeEditor
          Language="javascript"
          Title="JS"
          value={js}
          onChange={setJs}
        />
      </div>
      <div className="pane">
        <iframe
          srcDoc={srcDoc}
          title="output"
          sandbox="allow-scripts"
          width="100%"
          height="100%"
        />
      </div>
    </>
  );
}

export default App;
